export type AitMunicipality = (typeof MUNICIPALITIES)[number];
export type AitDistrict = AitMunicipality["districts"][number];

export function getMuniByName(name: string | null | undefined) {
  if (!name) return null;
  return MUNICIPALITIES.find((m) => m.name === name) ?? null;
}

export function getDistrictsForMuni(muniName: string | null | undefined) {
  const m = getMuniByName(muniName);
  return m ? [...m.districts] : [];
}

export function getSettlements(muniName: string | null | undefined, districtName: string | null | undefined) {
  const m = getMuniByName(muniName);
  if (!m) return [] as string[];
  const d = m.districts.find((x) => x.name === districtName);
  return d ? [...d.settlements] : [] as string[];
}
